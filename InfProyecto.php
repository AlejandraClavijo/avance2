        <h1>Información del Proyecto</h1>
        <div class="box">
            <h2>Misión</h2>
            <p>Proveer una plataforma integral y segura que facilite la gestión y el intercambio de conocimiento dentro de la comunidad universitaria de la Universidad Distrital José Francisco de Caldas. Nuestra misión es optimizar el proceso de aprendizaje y enseñanza a través de herramientas tecnológicas que permitan a estudiantes y profesores acceder, crear, compartir y aplicar conocimientos de manera efectiva y colaborativa.</p>
        </div>
        <div class="box">
            <h2>Visión</h2>
            <p>Ser la herramienta líder en gestión de conocimiento para instituciones educativas, reconocida por su capacidad para transformar el ambiente de aprendizaje en la Universidad Distrital José Francisco de Caldas. Aspiramos a expandir nuestro alcance, innovar constantemente y mantener la excelencia en servicio, para que nuestra comunidad estudiantil y académica se mantenga a la vanguardia en el ámbito educativo y profesional.</p>
        </div>
        <div class="box">
            <h2>Objetivos</h2>
            <h3>Objetivo General</h3>
            <p>Crear un ecosistema digital que centralice, facilite y promueva la gestión del conocimiento en la Universidad Distrital José Francisco de Caldas, apoyando el proceso educativo a través de la tecnología para mejorar la calidad y accesibilidad de la educación impartida y recibida por la comunidad universitaria.</p>
            <h3>Objetivos Específicos</h3>
            <ul>
                <li>Fomentar la Colaboración: Facilitar un espacio colaborativo donde estudiantes y docentes puedan compartir recursos, ideas y experiencias que contribuyan al aprendizaje colectivo.</li>
                <li>Optimizar el Acceso al Conocimiento: Proporcionar una plataforma intuitiva que permita a los usuarios acceder fácilmente a materiales educativos, recursos bibliográficos y herramientas de aprendizaje.</li>
                <li>Garantizar la Seguridad de la Información: Implementar protocolos de seguridad robustos para asegurar la privacidad y protección de los datos personales y académicos de los usuarios.</li>
                <li>Promover la Innovación Educativa: Estimular el uso de tecnologías emergentes y metodologías de enseñanza innovadoras que enriquezcan la experiencia educativa.</li>
                <li>Apoyo a la Investigación: Ofrecer una plataforma que sirva de apoyo para las actividades de investigación, permitiendo la organización y el acceso a información relevante y especializada.</li>
                <li>Evaluación y Mejora Continua: Establecer mecanismos de retroalimentación y análisis de datos que permitan la mejora continua de la plataforma, asegurando que se ajuste a las necesidades cambiantes de la comunidad universitaria.</li>
            </ul>
        </div>
        <div class="box">
            <h2>Problemáticas</h2>
            <ul>
                <li>Fragmentación de Conocimiento Específico: Los conocimientos y recursos especializados en análisis de sistemas están dispersos y no se comparten eficientemente dentro de la comunidad académica, lo que dificulta la formación integral de los estudiantes.</li>
                <li>Falta de Interoperabilidad: Los sistemas de información existentes operan de forma aislada y no se comunican entre sí, impidiendo una integración efectiva del conocimiento en análisis de sistemas.</li>
                <li>Actualización de Contenido en Análisis de Sistemas: La rápida evolución del campo de análisis de sistemas requiere una actualización constante del material didáctico y de investigación, lo cual no se logra con los sistemas tradicionales.</li>
                <li>Colaboración en Proyectos de Sistemas: No hay mecanismos adecuados para la colaboración en tiempo real en proyectos de análisis de sistemas, lo que limita la capacidad de trabajar en conjunto y compartir hallazgos relevantes.</li>
                <li>Aplicación Práctica de Conocimientos: Los estudiantes encuentran dificultades para aplicar teorías y conceptos de análisis de sistemas a situaciones prácticas debido a la falta de casos de estudio y proyectos interactivos disponibles.</li>
                <li>Deficiencia en Habilidades Analíticas: Los estudiantes requieren mejorar sus habilidades analíticas con ejercicios prácticos y retroalimentación constante, lo cual es insuficiente en los métodos de enseñanza actuales.</li>
                <li>Acceso a Herramientas de Análisis Actualizadas: Hay una carencia de acceso libre a herramientas y software de vanguardia para el análisis de sistemas, lo que limita la experiencia de aprendizaje y la preparación profesional de los estudiantes.</li>
                <li>Seguridad de Información Especializada: Los datos y proyectos de análisis de sistemas son sensibles y requieren de sistemas de seguridad robustos para proteger la propiedad intelectual y la privacidad de los involucrados.</li>
            </ul>
